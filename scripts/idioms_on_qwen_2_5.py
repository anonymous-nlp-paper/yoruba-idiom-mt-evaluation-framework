def load_dataset(path: str) -> pd.DataFrame:
    """Load and clean the Yorùbá idioms dataset."""
    df = pd.read_csv(path)
    df.columns = ['idiom', 'literal_meaning', 'idiomatic_meaning']
    df = df.dropna(subset=['idiom', 'idiomatic_meaning']).reset_index(drop=True)
    return df

